public class ascii {
    public static void main(String[] args) {
        char ch ='a' ;
        char box ='A';
        char Alphabet='z';
        int b = (int) ch;
        int c = (int) box;
        int d = (int) Alphabet;
        System.out.println("--ASCII VALUE OF CHARACTERS--");
        System.out.println("Ascii value of a : " + b);
        System.out.println("Ascii value of A : "+ c);
        System.out.println("Ascii value of z : "+ d);
    }
    
}
